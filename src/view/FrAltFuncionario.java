    private void gravar() {
        //validar os campos

        if (!verificarCampos()) {
            return;
        }
        //ler os campos
        //guardar em um objeto

        Funcionario f = new Funcionario();
        f.setPkFuncionario(Integer.parseInt(edtCodigo.getText()));
        f.setNome(edtNome.getText());
        f.setEmail(edtEmail.getText());
        f.setAtivo(rbtnAtivo.isSelected());

        // enviar para o banco de dados
        FuncionarioController controller = new FuncionarioController();
        if (controller.alterar(f)) {
            JOptionPane.showMessageDialog(null, "UsuÃ¡rio atualizado.");
            this.dispose();
        }

    }

    private boolean verificarCampos() {

        String senha = new String(edtSenha.getPassword());
        String confirmaSenha = new String(edtSenhaConfirma.getPassword());

        if (edtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo 'Nome' em branco.");
            return false;
        }

        if (edtNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo 'Email' em branco.");
            return false;
        }

        if (edtSenha.isEditable()) {
            if (new String(edtSenha.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Campo 'Senha'em branco.");
                return false;
            }
        }

        //^ - Inicio de linha
        //$ - Final de linha
        //[] - Conjunto de caracteres
        //+ - quantidade de vezes que o conjunto pode aparecer
        //+ - pelo menos 1 vez
        //* - 0 ou vÃ¡rias vezes o conjunto se repete
        //{X} - Especificamente X vezes
        if (!edtNome.getText().matches("[\\p{L} ]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Nome' possui formato invÃ¡lido");
            return false;
        }

        if (!edtEmail.getText().matches("^[a-z0-9_.]+@[a-z0-9_.]+.[a-z]+$")) {
            JOptionPane.showMessageDialog(null, "O campo 'Email' possui formato invÃ¡lido");
            return false;
        }

        if (edtSenha.isEditable()) {
            if (senha.length() < 6) {
                JOptionPane.showMessageDialog(null, "A senha deve ser maior que 6 dÃ­gitos.");
                return false;
            }
        }

        if (!senha.equals(confirmaSenha)) {
            JOptionPane.showMessageDialog(null, "As senhas devem ser idÃªnticas.");
            return false;
        }

        return true;
    }

